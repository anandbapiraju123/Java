package Kodnest_Java_Programming;
import java.util.*;
public class OperationsUsingShift {
	public static void main(String[] args) {
		Scanner s=new Scanner(System.in);
		int a=s.nextInt();
		System.out.println("multiplication of a with 2= "+(a<<1));
		System.out.println("division of a with 2= "+(a>>1));

	}

}
